package com.huaweicloud.hessian;

import org.springframework.boot.autoconfigure.condition.ConditionalOnProperty;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class HessianConfiguration {
  @Bean
  @ConditionalOnProperty(prefix = "spring.cloud.servicecomb.hessian", name = "enabled", matchIfMissing = true)
  public HessianHttpMessageConverter hessianHttpMessageConverter() {
    return new HessianHttpMessageConverter();
  }
}
